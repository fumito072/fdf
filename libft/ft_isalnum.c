#include "libft.h"

int	ft_isalnum(int num)
{
	if ('0' <= num && num <= '9')
		return (1);
	else if (('a' <= num && num <= 'z') || ('A' <= num && num <= 'Z'))
		return (1);
	else
		return (0);
}
